<template>
  <base-card>
    <h2>Login</h2>
    <p class="error" v-if="loginErrorMsg">Login error: {{ loginErrorMsg }}</p>
    <form @submit.prevent="submitForm">
      <div class="form-control">
        <label for="email">E-Mail</label>
        <input type="email" id="email" v-model.trim="email" />
      </div>
      <div class="form-control">
        <label for="password">Password</label>
        <input type="password" id="password" v-model.trim="password" />
      </div>
      <p
        v-if="!formIsValid"
      >Please enter a valid email and password (password must be at least 6 characters long).</p>
      <base-button>Login</base-button>
    </form>
  </base-card>
</template>

<script>
export default {
  data() {
    return {
      email: '',
      password: '',
      formIsValid: true,
      loginErrorMsg: '',
    };
  },
  methods: {
    async submitForm() {
      this.formIsValid = true;
      if (
        this.email === '' ||
        !this.email.includes('@') ||
        this.password.length < 6
      ) {
        this.formIsValid = false;
        return;
      }

      try {
        await this.$store.dispatch('login', {
          email: this.email,
          password: this.password,
        });
        
        // redirect to homepage after logged in
        this.$router.push({path: '/'});
      } catch (err) {
        this.loginErrorMsg = err.message || 'Something went wrong!';
      }
    }
  }
};
</script>

